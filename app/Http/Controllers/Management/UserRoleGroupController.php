    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function getByGroup($id)
    {
        $users = UserRoleGroup::where('group_id', $id)
            ->with('user_role', 'user_role.user', 'user_role.role', 'assistance_session')
            ->get()->toArray();

        return response()->json([
            'status' => true,
            'message' => 'Usuarios obtenidos exitosamente',
            'data' => ['user_role_group' => $users]
        ]);
    }
